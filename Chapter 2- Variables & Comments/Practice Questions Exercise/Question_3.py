#Write a python program that takes an input from user 
#and then type cast those values to string, int
#and float in the separate variables. Print all the variables.
# Get user input
User_Input = input("Enter a number: ")
# Typecast to string
String_Variable = str(User_Input)
# Typecast to integer
Int_Variable = int(User_Input)
# Typecast to float
Float_Variable = float(User_Input)
# Print the variables
print("As String:", String_Variable)
print("As Integer:", Int_Variable)
print("As Float:", Float_Variable)

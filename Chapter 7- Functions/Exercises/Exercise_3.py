# Define a function named make_shirt that takes a size and a message as arguments.
def make_shirt(size, message):
    """Summarize the shirt that's going to be made."""
    # Print a message describing the size and message on the shirt.
    print(f"\nI'm going to make a {size} t-shirt.")
    print(f'It will say, "{message}"')
# Call the function with different size and message values.
make_shirt('large', 'I love Python!')
make_shirt(size='medium', message="I love code lab 1 <3")
